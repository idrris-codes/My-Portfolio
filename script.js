const languageScreen = document.getElementById("languageScreen");
const cinemaIntro = document.getElementById("cinemaIntro");
const siteShell = document.getElementById("siteShell");
const floatingLang = document.getElementById("floatingLang");
const languageButtons = document.querySelectorAll(".language-choice");
const botsGrid = document.getElementById("botsGrid");
const websitesGrid = document.getElementById("websitesGrid");
const navToggle = document.getElementById("navToggle");
const navMenu = document.getElementById("navMenu");

let currentLang = "ru";
let introTimerA = null;
let introTimerB = null;

function getValue(path, obj) {
  return path.split(".").reduce((acc, key) => acc && acc[key], obj);
}

function renderProjects(container, items) {
  container.innerHTML = items.map((item, index) => {
    const disabled = !item.link;
    const delay = index === 0 ? "" : index === 1 ? " delay-1" : " delay-2";
    const tags = item.tags.map(tag => `<span>${tag}</span>`).join("");

    return `
      <article class="project-card reveal${delay}${disabled ? " disabled" : ""}">
        <div class="project-meta">
          <span>${item.type}</span>
          <span class="status">${item.status}</span>
        </div>
        <h3>${item.title}</h3>
        <p>${item.desc}</p>
        <div class="tags">${tags}</div>
        <a class="btn ghost" href="${item.link || '#websites'}" ${item.link ? 'target="_blank" rel="noopener"' : ''}>${item.button}</a>
      </article>
    `;
  }).join("");
}

function setLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang === "tj" ? "tg" : lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getValue(element.dataset.i18n, translations[lang]);
    if (value) element.textContent = value;
  });

  renderProjects(botsGrid, translations[lang].data.bots);
  renderProjects(websitesGrid, translations[lang].data.websites);
  refreshReveal();
}

function closeNav() {
  navToggle.classList.remove("active");
  navMenu.classList.remove("open");
  navToggle.setAttribute("aria-expanded", "false");
}

function openPortfolio(lang) {
  clearTimeout(introTimerA);
  clearTimeout(introTimerB);
  setLanguage(lang);
  closeNav();
  document.body.classList.add("locked");
  languageScreen.classList.add("hidden");
  cinemaIntro.classList.add("play");

  introTimerA = setTimeout(() => {
    siteShell.classList.add("visible");
    document.body.classList.remove("locked");
    refreshReveal();
  }, 2350);

  introTimerB = setTimeout(() => {
    cinemaIntro.classList.remove("play");
  }, 2950);
}

function showLanguageScreen() {
  languageScreen.classList.remove("hidden");
  document.body.classList.add("locked");
  closeNav();
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => openPortfolio(button.dataset.lang));
});

floatingLang.addEventListener("click", showLanguageScreen);

navToggle.addEventListener("click", () => {
  const isOpen = navMenu.classList.toggle("open");
  navToggle.classList.toggle("active", isOpen);
  navToggle.setAttribute("aria-expanded", String(isOpen));
});

const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("show");
      observer.unobserve(entry.target);
    }
  });
}, {
  threshold: 0.12,
  rootMargin: "0px 0px -40px 0px"
});

function refreshReveal() {
  document.querySelectorAll(".reveal").forEach((element) => {
    if (!element.classList.contains("show")) observer.observe(element);
  });
}

function smoothScrollTo(target) {
  const navHeight = document.querySelector(".navbar")?.offsetHeight || 90;
  const targetPosition = target.getBoundingClientRect().top + window.scrollY - navHeight - 18;
  window.scrollTo({ top: targetPosition, behavior: "smooth" });
}

document.addEventListener("click", (event) => {
  const anchor = event.target.closest('a[href^="#"]');
  if (!anchor) return;
  const id = anchor.getAttribute("href");
  const target = document.querySelector(id);
  if (!target) return;
  event.preventDefault();
  smoothScrollTo(target);
  closeNav();
});

window.addEventListener("mousemove", (event) => {
  const x = event.clientX / window.innerWidth - 0.5;
  const y = event.clientY / window.innerHeight - 0.5;
  document.documentElement.style.setProperty("--mx", `${x * 18}px`);
  document.documentElement.style.setProperty("--my", `${y * 18}px`);
});

let mobileMotion = 0;
function animateMobileBackground() {
  if (window.matchMedia("(hover: none) and (pointer: coarse)").matches) {
    mobileMotion += 0.006;
    const x = Math.sin(mobileMotion) * 14;
    const y = Math.cos(mobileMotion * 0.86) * 14;
    document.documentElement.style.setProperty("--mx", `${x}px`);
    document.documentElement.style.setProperty("--my", `${y}px`);
  }
  requestAnimationFrame(animateMobileBackground);
}
animateMobileBackground();

window.addEventListener("resize", () => {
  if (window.innerWidth > 820) closeNav();
});

setLanguage(currentLang);
refreshReveal();
document.body.classList.add("locked");
